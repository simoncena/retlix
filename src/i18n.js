const STORE_KEY = 'retlix-lang';

function detectLang() {
  try {
    const saved = localStorage.getItem(STORE_KEY);
    if (saved && (saved === 'it' || DICT[saved])) return saved;
    const nav = (navigator.language || 'it').slice(0, 2).toLowerCase();
    if (nav === 'it' || DICT[nav]) return nav;
  } catch {}
  return 'it';
}

const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [lang, setLangState] = useState(detectLang);

  const setLang = useCallback((code) => {
    setLangState(code);
    try { localStorage.setItem(STORE_KEY, code); } catch {}
    try { document.documentElement.lang = code; } catch {}
  }, []);

  const t = useCallback((str, params) => {
    let out = (lang === 'it' ? str : (DICT[lang]?.[str] ?? str));
    if (params) for (const k of Object.keys(params)) out = out.replaceAll(`{${k}}`, params[k]);
    return out;
  }, [lang]);

  const value = useMemo(() => ({ lang, setLang, t, languages: LANGUAGES }), [lang, setLang, t]);
  // createElement (not JSX) so this stays a plain .js module that Vite imports anywhere.
  return React.createElement(I18nContext.Provider, { value }, children);
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  // Fallback so components don't crash if used outside the provider (identity t).
  return ctx || { lang: 'it', setLang: () => {}, t: (s, p) => { let o = s; if (p) for (const k in p) o = o.replaceAll(`{${k}}`, p[k]); return o; }, languages: LANGUAGES };
}
